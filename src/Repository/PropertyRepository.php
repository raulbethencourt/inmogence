class PropertyRepository extends ServiceEntityRepository
{
    /**
     * @var PaginatorInterface
     */
    private $paginator;

    public function __construct(ManagerRegistry $registry, PaginatorInterface $paginator)
    {
        parent::__construct($registry, Property::class);
        $this->paginator = $paginator;
    }

    /**
     * @param \App\Entity\PropertySearch $search
     * @return \Knp\Component\Pager\Pagination\PaginationInterface
     */
    public function paginateAllVisible(PropertySearch $search, int $page): PaginationInterface
    {
        $query =  $this->findVisibleQuery();

        if ($search->getOptions()->count() > 0) {
            $k = 0;
            foreach ($search->getOptions() as $option) {
                $k++;
                $query = $query
                    ->andWhere(":option$k MEMBER OF p.options")
                    ->setParameter("option$k", $option);
            }
        }

        if ($search->getMaxPrice()) {
            $query = $query
                ->andWhere('p.price < :maxprice')
                ->setParameter('maxprice', $search->getMaxPrice());
        }

        if ($search->getMinSurface()) {
            $query = $query
                ->andWhere('p.surface >= :minsurface')
                ->setParameter('minsurface', $search->getMinSurface());
        }

        $properties = $this->paginator->paginate(
            $query->getQuery(),
            $page,
            12
        );

        return $properties;
    }

    /**
     * @return Property[]
     */
    public function findLatest(): array
    {
        return $this->findVisibleQuery()
            ->setMaxResults(4)
            ->getQuery()
            ->getResult();
    }

    /**
     * @return ORMQueryBuilder
     */
    private function findVisibleQuery(): ORMQueryBuilder
    {
        return $this->createQueryBuilder('p')
            /*->select('p', 'opt')
            ->leftJoin('p.options', 'opt')*/
            ->where('p.sold = false');
    }

